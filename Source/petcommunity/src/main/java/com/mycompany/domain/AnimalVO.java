package com.mycompany.domain;

public class AnimalVO {
	private int animalId;

	private String animalName;
	private String animalBirthday;
	private String animalGender;
	private String animalFeature;
	
	public int getAnimalId() {
		return animalId;
	}
	public void setAnimalId(int animalId) {
		this.animalId = animalId;
	}
	public String getAnimalName() {
		return animalName;
	}
	public void setAnimalName(String animalName) {
		this.animalName = animalName;
	}
	public String getAnimalBirthday() {
		return animalBirthday;
	}
	public void setAnimalBirthday(String animalBirthday) {
		this.animalBirthday = animalBirthday;
	}
	public String getAnimalGender() {
		return animalGender;
	}
	public void setAnimalGender(String animalGender) {
		this.animalGender = animalGender;
	}
	public String getAnimalFeature() {
		return animalFeature;
	}
	public void setAnimalFeature(String animalFeature) {
		this.animalFeature = animalFeature;
	}
	
	
	
}
